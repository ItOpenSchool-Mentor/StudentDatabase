# Arithmetic Operators
print(f"5 + 3 : {5 + 3}")
print(f"5 + 3.0 : {5 + 3.0}") # 8.0
print(f"5 -  3 : {5 - 3}")
print(f"5 / 2 : {5 / 2}")  #2.5
print(f"4 / 2 : {4 / 2}")  #2.0
print(f"5 // 2 : {5 // 2}") #2
print(f"-5 // 2 : {-5 // 2}") #-3
print(f"5 % 2 : {5 % 2}")  #1
print(f"5 * 2 : {5 * 2}")  #10
print(f"5 ** 3 : {5 ** 3}")  #125




