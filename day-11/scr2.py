# Assignment Operators
# simple assignment operator =
x = 5
print(f"x = 5 : {x}")
print("-" * 30)

# add and assign +=
x = 3
print(f"x = {x}, after += 5, Value= ", end = "")
x += 5
print(x)
print("-" * 30)

# multiply and assign *=
x = 3
print(f"x = {x}, after *= 5, Value= ", end = "")
x *= 5
print(x)
print("-" * 30)
