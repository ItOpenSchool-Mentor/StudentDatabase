# 4 ways to create a String Value[Object]
str1 = "Hello World"  # Way-1 : Using Double Quotation Marks
str2 = 'Hi there'        # Way-2 : Using Single   Quotation Marks

# Way-3 : Using Multiline String - 3 Double Quot. Marks
str3 = """
Hello Viewers
We are into Day-6 Session
Great Goin
"""

# Way-4 : Using Multiline String - 3 Single Quot. Marks
str4 = '''
Hi Students
Welcome back to Day-6 Session
'''
print("Way-1, Using Double Quotation Marks : ", str1)
print("Way-2, Using Single  Quotation Marks : ", str2)
print("Way-3, Using Multiline Double Quotation Marks : ", str3)
print("Way-4, Using Multiline Single  Quotation Marks : ", str4)
