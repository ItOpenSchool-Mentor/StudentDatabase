# List SLICING [start : stop/end : step]
a = [1, 2, 3, 4, 5]

print(f"a[1:3] : {a[1:3]}")
print("-" * 50)

print(f"a[::2] : {a[::2]}")
print("-" * 50)

print(f"a[::-1] : {a[::-1]}")
print("-" * 50)

print(f"a[4:1:-1] : {a[4:1:-1]}")
print("-" * 50)

print(f"a[3:1:-2] : {a[3:1:-2]}")
print("-" * 50)

print(f"a[3:1:-1] : {a[3:1:-1]}")
print("-" * 50)
